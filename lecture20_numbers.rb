#Working with numbers in Ruby
puts "What's your first number?"
first_number = gets.chomp.to_i

puts "What's your second number?"
second_number = gets.chomp.to_i

#Multiplication
puts "The result of multiplying your first and second numbers is #{first_number * second_number}"

#Subtraction
puts "The result of subtracting your first and second numbers is #{first_number - second_number}"

#Division
puts "The result of dividing your first from your second numbers is #{first_number / second_number}"

#Modulo
puts "The modulo of your first and second numbers is #{first_number % second_number}"


